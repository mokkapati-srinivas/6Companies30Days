class Solution
{
    //Function to find the maximum money the thief can get.
    public int FindMaxSum(int arr[], int n)
    {
        // Your code here
        int dp[]=new int[n];
        for(int i=0;i<n;i++)
        {
            int minus2=(i-2>=0)?dp[i-2]:0;
            int minus1=(i-1>=0)?dp[i-1]:0;
            dp[i]=Math.max(minus2+arr[i],minus1);
        }
        
        return dp[n-1];
    }
}